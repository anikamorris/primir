import Foundation
import UIKit

// Thanks to Bram De Geyter (https://stackoverflow.com/users/2042527/bram-de-geyter) for this helpful color extension!
extension UIColor {
    static var placeholderGray: UIColor {
        return UIColor(red: 0, green: 0, blue: 0.0980392, alpha: 0.22)
    }
}
